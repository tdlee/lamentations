        <div class="poem__verseref">1</div>
        <p>
          Alas!<sup>[1]</sup> The gold, now dulled;<br>
          &emsp;finest gold tarnished!<br>
          the holy stones<sup>[2]</sup> lie littered<br>
          &emsp;on every street corner.
        </p>

        <br>
        <div class="poem__verseref">2</div>
        <p>
          Belov&egrave;d sons of Zion<br>
          &emsp;of gold-weight worth,<br>
          alas, rated as crocks of clay&mdash;<br>
          &emsp;work of any potter.
        </p>

        <br>
        <div class="poem__verseref">3</div>
        <p>
          Cubs even of jackals<br>
          &emsp;are breast-fed nursed,<br>
          but my Daughter People is cruel<br>
          &emsp;as the desert ostrich.<sup>[3]</sup>
        </p>

        <br>
        <div class="poem__verseref">4</div>
        <p>
          Dry-thirsted, the infant's tongue<br>
          &emsp;glues to the palate;<br>
          babies beg for bread<br>
          &emsp;but none is proffered.
        </p>

        <br>
        <div class="poem__verseref">5</div>
        <p>
          Embraced now on dung heaps are those<br>
          &emsp;once robed in purple;<sup>[4]</sup><br>
          Those once feasting on sweetmeats<br>
          &emsp;lie destitute in alleys.
        </p>

        <br>
        <div class="poem__verseref">6</div>
        <p>
          Far more the deserts of my people<br>
          &emsp;than the penalty of Sodom,<br>
          which had been in an instant erased<br>
          &emsp;with no hand on her laid.
        </p>

        <br>
        <div class="poem__verseref">7</div>
        <p>
          Glistening as snow, once, her princes,<br>
          &emsp;whiter than milk;<br>
          their limbs more ruddy than coral,<br>
          &emsp;their beauty as sapphire.
        </p>

        <br>
        <div class="poem__verseref">8</div>
        <p>
          How black now, though, their faces,<br>
          &emsp;unrecognised in streets;<br>
          their skin shrunk, taut on their bones<br>
          &emsp;as dessicated wood.
        </p>

        <br>
        <div class="poem__verseref">9</div>
        <p>
          Impaling on sword? Far better<br>
          &emsp;than piercing by famine.<br>
          Better to bleed from our wounds<br>
          &emsp;than be starved of all food. See&mdash;
        </p>

<!--
