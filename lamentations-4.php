        <br>
        <div class="poem__verseref">10</div>
        <p>
          Large-hearted women's own hands<br>
          &emsp;cook their own children!<br>
          Their children their food at the holocaust<br>
          &emsp;of my Daughter People.<!-- 2:11, 3:48, 4:10 -->
        </p>

        <br>
        <div class="poem__verseref">11</div>
        <p>
          Meted out in full is the <span class="yhwh">Lord</span>'s anger,<br>
          &emsp;his wrath disgorged;<br><!-- disgorged: 4:1, 4:11, 4:13 -->
          He has kindled a blaze in Zion<br>
          &emsp;that consumed her foundations.
        </p>

        <br>
        <div class="poem__verseref">12</div>
        <p>
          Not the kings of the earth<br>
          &emsp;nor its people believed<br>
          that an enemy or foe could pass through<br>
          &emsp;the gates of Jerusalem.
        </p>

        <br>
        <div class="poem__verseref">13</div>
        <p>
          Offences of prophets, the cause,<br>
          &emsp;and the iniquities of her priests,<br><!-- 4:6,13: same nouns, reverse order; 6:singular; 13:plural -->
          who had disgorged in the midst of the city<br><!-- disgorged: 4:1, 4:11, 4:13 -->
          &emsp;the blood of the just.
        </p>

        <br>
        <div class="poem__verseref">14</div>
        <p>
          Polluted with blood, through the streets<br>
          &emsp;they grope blindly;<br>
          so defiled that none would dare<br>
          &emsp;touch their garments.
        </p>

        <br>
        <div class="poem__verseref">15</div>
        <p>
          "Repulsive! Away!"<br>
          &emsp;[people shout at them.]<br>
          "Replusive! Repulsive! Hands off!"<br>
          &emsp;So they straggled; they strayed.<br><!-- Hebrew alliteration -->
          [Resolute were the nations:]<br>
          &emsp;"They'll not reside here."<sup>[6]</sup>
        </p>

        <br>
        <div class="poem__verseref">16</div>
        <p>
          Scattered by the <span class="yhwh">Lord</span> himself:<br>
          &emsp;no longer he faces them.<br>
          The priests are shown no honour;<br>
          &emsp;the elders, no favour.
        </p>

        <br>
        <div class="poem__verseref">17</div>
        <p>
          Teared eyes still pine; looking<br>
          &emsp;for help, but in vain;<br>
          From our watchtowers we watched for a nation;<br>
          &emsp;but no rescue came.
        </p>

        <br>
        <div class="poem__verseref">18</div>
        <p>
          Unceasing, they stalked all our steps,<br>
          &emsp;our streets no more ours.<br>
          Our end drew near, our days gone;<br>
          &emsp;our end had come.
        </p>

        <br>
        <div class="poem__verseref">19</div>
        <p>
          Vicious and swifter than sky-hawks<br>
          &emsp;came our pursuers:<br>
          Hot on our trail through the hills;<br>
          &emsp;ambushers of the desert.
        </p>

        <br>
        <div class="poem__verseref">20</div>
        <p>
          Wedged in their traps was our life-breath,<br>
          &emsp;the <span class="yhwh">Lord</span>'s own anointed&mdash;<br>
          He in whose shade we had thought<br>
          &emsp;to dwell among nations.
        </p>

        <br>
        <div class="poem__verseref">21</div>
        <p>
          You might rejoice now, Daughter Edom<br>
          &emsp;gloating in Uz;<br>
          but to you, too, the chalice shall pass,<br>
          &emsp;exposing you, drunk-shamed.
        </p>

        <br>
        <div class="poem__verseref">22</div>
        <p>
          Zion-Daughter, your punishment ended,<br>
          &emsp;your exile is lifted;<br>
          but you, Daughter Edom, found guilty,<br>
          &emsp;the <span class="yhwh">Lord</span> shall condemn.
        </p>

