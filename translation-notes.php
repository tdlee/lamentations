    <h2>Translation? Or something else?</h2>

    <p>
      I would like to call this a translation.
      But that opens up the thorny question of that term's definition.
      Getting hung up on the term itself would probably only take us down a long, dark rabbit-hole to a dead end.
    </p>

    <p>
      I have endeavoured to use the principles of translation.
      But whereas translation of ancient secular poetry allows some freedom to include considerations of poetic features,
      biblical translation, by contrast, places high regard on treating texts,
      even poetic texts, as prose, where accurate dictionary-equivalence is paramount;
      it makes relatively minor allowance for poetic form.

    <p>
      That creates a tension here in Lamentations, which is strongly poetic.
      And this translation emphasises this inherent, structural poetry as a defining characteristic.
      Certainly, the acrostic, including its "A" and "Z" endpoints, is key.
      Equally key is the underlying rhythm:
      that incessantly driving 3&ndash;2 'qinah' pulse of the first four chapters.
      To the best of my knowledge no other translation of Lamentations has both.
    </p>

    <p>
      So is it a translation?  A paraphrase?  A version?  A rendering?  Let the reader decide.
    </p>

    <h2>Guiding principles</h2>

    <p>
      The alphabetic acrostic is central.  The rhythm is central.
      But what about the text?
    </p>

    <p>
      Hebrew poetry tends not to do rhyme.
      To add rhyme here, despite its prevalence in English poetry and hymnody,
      would gain very little, and would force far more constraint than is necessary.
      Accordingly this version makes no attempt at rhyme.
      Indeed, on a couple of occasions,
      the drafting process accidentally produced rhyme;
      I then specifically redrafted in order to avoid its distraction.
    </p>

    <p>
      But Hebrew poetry <em>does</em> make quite frequent use of wordplay.
      Sometimes, something similar can be done in English translation;
      alliteration can be useful here.
    </p>

    <p>
      Both Hebrew and English use metaphor.
      Where reasonably possible, this is conveyed,
      although achieving it sometimes requires substitution.
    </p>

    <p>
      Often, subtle aspects of wordplay are untranslatable.
      But conversely it is sometimes possible to introduce English poetic features in unrelated places elsewhere.
      So, taking the bigger picture into account,
      an unavoidable loss of detail in one place
      may be compensated by an introduced poetic expression in another place.

    <h2>Acrostic challenges</h2>

    <p>
      Acrostics constrain the choice of a verse's opening word.
      Sometimes there simply isn't an English word anywhere near suitable.
      One approach can then be to swap lines within a verse.
      A case in point is 1:7 whose first line requires an opening "G" word.
      A workable solution is to switch its first and second lines,
      allowing for "great treasures" to begin the verse.
    </p>

    <p>
      But a chapter later at 2:7 a similar problem arises,
      and also, as it happens, with a "G" verse.
      Here, too, a similar line-swap solution offered itself.
      This was tempting.
      But other factors argued against its adoption,
      despite the fact that it would solve an additional problem.
      See the footnote there.
    </p>

    <p>
      The acrostic use of "Z" for the final stanza of the first four poems
      is an interesting challenge, as there are so few such English words
      in regular use.
      The third poem in particular requires the use of three such words.
      These must all be different, to reflect the Hebrew "tav" words being different.
    </p>

    <p>
      Conversely, the third poem's ninth stanza,
      Hebrew letter "tet",
      requires a single word used three times,
      ideally meaning "good".
      But "good" is not available to us,
      as "g" is the seventh, not ninth, letter in our alphabet.
      We need an "I" word that is reasonably synonymous with "good".<?php
        Footnote('The other possibility would have been to make use of our needing to omit four letters.  That would allow pushing out to "J" or even "K".  But the smaller number of such words would only have made the problem harder, not easier.');
      ?>
      "Irreproachable" seems a reasonable choice, with not too much semantic compromise,
      although its five syllables rather than one pull against our desired linguistic compactness.
    </p>

    <p>
      In the triple acrostic at 3:43&ndash;45
      the first two verses have noticeably similar structures,
      both leading with a powerfully pictorial verb meaning "cover", "veil", "screen" or "envelop".
      At this point we need an "R" word, but there is no readily apparent such verb,
      nor is there another word suitably shareable across them both.
      So the unusual, but clear "re-cloak" was used.
      While its "re-" prefix suggests a concept of recurrence
      which is not apparent in the original verbs,
      nevertheless it hints back to ideas to which the wider context
      of those verses seems to allude.
      See the footnote there.
    </p>

    <p>
      On the positive side, our 26 letters over Hebrew's 22
      means we can drop four English letters.
      This provides some flexibility.
      'X' is an obvious candidate.  'Z' would also have been,
      but our strong desire for that sense  of "A&nbsp;to&nbsp;Z" totality
      requires its inclusion.
    </p>

    <h3>A thematic example</h3>

    <p>
      There are a few cases of words being repeated within a poem,
      and in some cases across them.
      I try to retain these.
    </p>

    <p>
      The verbs "look" and "notice" recur, sometimes in emphatic pairing.
      Other English verbs would have been candidates: "see", "behold", "observe" etc.
      But our acrostic requirement drives the choice, particularly of "notice"
      because 1:12 demands a strong, imperative verb at its start,
      which has to be at, or very close to, alphabetic 'n'.
      But there is more.  The run up to that 1:12 prepares the way:
    </p>
    <ul>
    <li>
      1:9<i>c</i>: &hellip;<strong>Look</strong>, O&nbsp;<span class="yhwh">Lord</span>: my affliction&hellip;
    </li>
    <li>
      1:11<i>c</i>: &hellip;<strong>Look</strong>, O&nbsp;<span class="yhwh">Lord</span>, and <strong>notice</strong>&hellip;
    </li>
    <li>
      1:12: <strong>Notice!</strong> And <strong>look</strong>, you passers-by&hellip;
    </li>
    </ul>
    <p>
      Then observe that 2:20 uses it,
      and even more that the final poem almost opens with that same pair of verbs.
      The "notice" root also puts in an appearance at 3:63 and 4:16.
    </p>
    <ul>
    <li>
      2:20: &hellip;<strong>Look</strong>, <span class="yhwh">Lord</span>; <strong>notice</strong>&hellip;
    </li>
    <li>
      3:63: You <strong>notice?</strong>&hellip;
    </li>
    <li>
      4:16: &hellip;no longer he <strong>takes note</strong>&hellip;
    </li>
    <li>
      5:1: &hellip;<strong>look</strong>; <strong>notice</strong> our disgrace&hellip;
    </li>
    </ul>

    <p>
      But driving all that was the acrostic requirement at 1:12 for an 'n' verb.
    </p>

    <h2>Qinah</h2>

    <p>
      <em>Qi<u>nah</u> or <u>not</u> qi<u>nah</u>? <u>That</u> is the <u>ques</u>tion.</em>
    </p>

    <p>
      Most biblical scholarship is agreed beyond reasonable doubt
      that the 3&ndash;2 qinah beat is prevalent in the first four chapters.
      But there is lively debate about particular details in particular verses.
    </p>

    <p>
      For example, the opening "Alas!" verse of the first chapter,
      and probably also of the second, may be seen as 4&ndash;2.
      Accordingly some of the rhythmically alert translations
      set that single-beat "Alas!" as a separate anacrusis.
      This version, too, adopts that practice.
    </p>

    <p>
      Much of this gnat-straining debate will almost certainly never be unambiguously or satisfactorily resolved.
      In view of that, I simply endeavour to use qinah as consistently as possible in these chapters.
      This also helps highlight the contrast on entering chapter&nbsp;5,
      where there is reasonable agreement that its rhythm is mostly non-qinah, often 3&ndash;3.
    </p>

    <h2>Inclusive language</h2>

    <p>
      Contemporary writing and translations rightly veer towards inclusive language, such as preferring "humankind" for "mankind".
      In general, this version adopts that principle.<?php
        Footnote('One specific exception is at 4:1&ndash;2 where "sons" is used to allow a carrying forward from the Hebrew of its "stones&hellip;sons" wordplay which is embedded in a larger-scale metaphor in those verses. See the further footnote there.');
      ?>
    </p>

    <p>
      Nevertheless, gender-specificity is a strong feature of some of the Lamentations poetry.
      The "Daughter Zion" and "Daughter Jerusalem" personalisation is integral to chapters 1 and 2.
      Similarly, the "strong man" characterisation recurs through chapter 3.
      In these contexts, attempting to downplay these characteristics
      would seem not only pointless but even counter-productive.
      Accordingly this version maintains this distinction.
    </p>
    <h2>Word choices</h2>

    <p>
      Lamentations was written in contemporary language.
      Accordingly this version sometimes uses words that are relatively modern
      or that have a modern edge.
      Examples that carry additional relevance from recent history, particularly <abbr title="World War 2">WW2</abbr>, are:
    </p>
    <ul>
      <li>
        blitzed (2:2): imported into English from the German "blitzkrieg".
      </li>
      <li>
        firestorm (4:11): an older word that gained particular currency from Allied bombing raids.
      </li>
      <li>
        holocaust (2:11, 3:47,48, 4:10): while an old word, this gained specific usage following the gas-chamber slaughter of Jewish people and others.
      </li>
    </ul>

    <p>
      Other examples include: "zero in" (1:22), "hell-bent" (2:8), "zilch" (3:64), "ziplock" (3:65), "boozed" (4:21).
    </p>

    <p>
      Meanwhile the verb "zoned" (2:22) is old, whose meanings include "encicle".
    </p>

    <h3>Word choices: a worked example</h3>

    <p>
      The very first line, 1:1,  "Alas! Alone she lies: the city" demonstrates substitution.
      The original verb is more literally "sits",
      and most prose-oriented English translations use this.
      But in English, we tend to think of such a city as "lying desolate" rather than "sitting desolate".  For us, "lie" is a better poetic metaphor than "sits".
      Further, and more subtly, the same verb is used a little later in the middle of v.3.  This would be "&hellip;sits among the nations".
      But again, that doesn't really work in English.  Yet neither would "&hellip;lies among the nations".
    </p>

    <p>
      Part of the problem is that the original "sits" has an additional shade of meaning that can incorporate something like "dwells" or "resides".
      And the two uses here allow one common Hebrew verb to express both ideas.
      But no single English verb captures this.
    </p>

    <p>
      So in v.3, I use "lodges among the nations".
      While I am now using two different verbs where the original has one common verb,
      nevertheless the pairing of "lies" and "lodges" lends alliterative commonality and association.
    </p>

    <p>
      Also, the use of "lies" in v.1 allows a further audible resonance
      between "she lies" and "she&hellip;lady".
    </p>

    <h3>Other examples</h3>

    <ul>
    <li>
      At 1:3 the geographic word here translated as "straits" (or "narrows") cannot capture the Hebrew word's resonance with their word for "Egypt".<?php Footnote("Berlin (2004) p.51; Goldingay (2022) p.54 note 'e'"); ?>
      To the original hearers, this would have conveyed a powerful reminder, alas lost to us, of other dire times in another place.
    </li>

    <li>
      At 5:18, desolate Zion is vividly pictured as occupied by wild animals.
      Other translations variously use "foxes" or "jackals";
      ancient zoological classifications are sometimes ambiguous to us.
      Here, I use "haunt of hyenas", which maintains the broad zoological classification
      and allows the use of a suitably unsettling alliterative expression.
    </li>
    </ul>

