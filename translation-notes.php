          </p>
        </div>
      </div>

      <div class="panel__cite">
          &mdash;Lam.1:12, NRSV
      </div><!-- "panel__cite" -->
    </div>

    <p>
      The obvious acrostic-leading 'n'-word would be that "nothing" in the first part of the first couplet.
      Despite that, there seems no decent, readable means to re-organise the couplet
      to start with "<span class="acrostic-letter">N</span>othing&hellip;" or similar.
      But its second part is "look and see".
      This would allow the 'n'-word "<span class="acrostic-letter">N</span>otice"
      and presents a possible way forward:
    </p>

    <div class="panel panel--width-22em">
      <div class="poem poem__hebverse poem--acrostic">
        <div class="poem__verse">
          <div class="poem__versetext">
            <p>
              Notice! And look, you passers-by;<br>
                &emsp;is it nothing to you?<br>
              Is there any&hellip;
            </p>
          </div><!-- poem__versetext -->
        </div><!-- poem__verse -->
      </div><!-- "poem" -->
    </div><!-- "panel" -->

    <p>
      But there is more.
      The verbs and their frequent pairing present a recurrent theme,
      not only within the first chapter but across other chapters.
      So when locking in the acrostic  "<span class="acrostic-letter">N</span>otice"
      these, too, must be considered.
      The run up to that 1:12 prepares the way:
    </p>
    <ul>
    <li>
      1:9<i>c</i>: &hellip;<strong>Look</strong>, O&nbsp;<span class="yhwh">Lord</span>: my affliction&hellip;
    </li>
    <li>
      1:11<i>c</i>: &hellip;<strong>Look</strong>, O&nbsp;<span class="yhwh">Lord</span>, and <strong>notice</strong>&hellip;
    </li>
    <li>
      1:12: <strong>Notice!</strong> And <strong>look</strong>, you passers-by&hellip;
    </li>
    <li>
      1:20: &hellip;<span class="yhwh">Lord</span>; <strong>look!</strong>
    </li>
    </ul>
    <p>
      Then observe that 2:20 uses it,
      and even more that the final poem almost opens with that same pair of verbs.
      The "notice" root also puts in an appearance at 3:63 and 4:16,
      and "look" at 2:16 and 3:36:
    </p>
    <ul>
    <li>
      2:16: we have lived to <strong>look</strong> upon it
    </li>
    <li>
      2:20: &hellip;<strong>Look</strong>, <span class="yhwh">Lord</span>; <strong>notice</strong>&hellip;
    </li>
    <li>
      3:36: does our Lord not <strong>look?</strong>
    </li>
    <li>
      3:63: You <strong>notice?</strong>&hellip;
    </li>
    <li>
      4:16: &hellip;no longer he <strong>takes note</strong>&hellip;
    </li>
    <li>
      5:1: &hellip;<strong>look</strong>; <strong>notice</strong> our disgrace&hellip;
    </li>
    </ul>

    <p>
      Further, <strong>look</strong> should also be in 3:1,
      where the man's opening follows hard on the heels of Daughter Zion's
      commanding use of it, 2:20, in her brief but devasting speech.
      Sadly, triple constraints
      conspire to put its incorporation beyond reach:
      the acrostic (which tended to expand the line),
      the <em>qinah</em> rhythm (which needs to constrain the line)
      and the absolute requirement to have "I am the man" as close as possible to the start.<?php Footnote('It would be something like "I am the man who has looked [squarely] at affliction" which is both non-acrostic and non-<em>qinah</em>.'); ?>
      Also, the Hebrew verb carries the additional nuance of "to experience, know",
      important to this instance of its use.<?php Footnote('Dobbs-Allsopp (2012), p.111.'); ?>
    </p>

    <p>
      But driving all that was the requirement at 1:12 for a leading acrostic 'n'-word.<?php
        Footnote('Ideally, the leading verb at 3:59 and 60, which are W-acrostic, would also be included here.  But that was a stretch too far, so I used the visual near-synonym "witness".');
      ?>
    </p>

    <h2>Qinah</h2>

    <p>
      <em>Qi<u>nah</u> or <u>not</u> qi<u>nah</u>? <u>That</u> is the <u>ques</u>tion.</em>
    </p>

    <p>
      Most biblical scholarship is agreed beyond reasonable doubt
      that the 3&ndash;2 <em>qinah</em> beat is prevalent in the first four chapters.
      But there is lively debate about particular details in particular verses.
    </p>

    <p>
      For example, the opening "Alas!" verse of the first chapter,
      and probably also of the second, may be seen as 4&ndash;2.
      Accordingly some of the rhythmically alert translations
      set that single-beat "Alas!" as a separate anacrusis.
      This version, too, adopts that practice.
    </p>

    <p>
      Much of this gnat-straining debate will almost certainly never be unambiguously or satisfactorily resolved.
      In view of that, I simply endeavour to use <em>qinah</em> as consistently as possible in these chapters.
      This also helps highlight the contrast on entering chapter&nbsp;5,
      where there is reasonable agreement that its rhythm is mostly non-<em>qinah</em>, often 3&ndash;3.
    </p>

    <h2>Inclusive language</h2>

    <p>
      Contemporary writing and translations rightly veer towards inclusive language, such as preferring "humankind" for "mankind".
      In general, this version adopts that principle.<?php
        Footnote('One specific exception is at 4:1&ndash;2 where "sons" is used to allow a carrying forward from the Hebrew of its "stones&hellip;sons" wordplay which is embedded in a larger-scale metaphor in those verses. See the further footnote there.');
      ?>
    </p>

    <p>
      Nevertheless, gender-specificity is a strong feature of some of the Lamentations poetry.
      The "Daughter Zion" and "Daughter Jerusalem" personalisation is integral to chapters 1 and 2.
      Similarly, the "strong man" characterisation recurs through chapter 3.
      In these contexts, attempting to downplay these characteristics
      would seem not only pointless but even counter-productive.
      Accordingly this version maintains this distinction.
    </p>
    <h2>Word choices</h2>

    <p>
      Lamentations was written in contemporary language.
      Accordingly this version sometimes uses words and terms that are relatively modern
      or that have a modern edge.
      Examples that carry additional relevance from recent history, particularly <abbr title="World War 2">WW2</abbr>, are:
    </p>
    <ul>
      <li>
        blitzed (2:2): imported into English from the German "blitzkrieg".
      </li>
      <li>
        firestorm (4:11): an older word that gained particular currency from Allied bombing raids.
      </li>
      <li>
        holocaust (2:11, 3:47,48, 4:10): while an old word, this gained specific usage following the gas-chamber slaughter of Jewish people and others.
      </li>
    </ul>

    <p>
      Other examples include: "zero in" (1:22), "hell-bent" (2:8), "slow-clap" (2:15), "snide-song" (3:63), "zilch" (3:64), "ziplock" (3:65), "boozed" (4:21), "up to our necks" (5:5).
      And "ranked" (4:2) also now carries an additional modern slant of disdain.
    </p>

    <p>
      Meanwhile the verb "zoned" (2:22) is old, whose meanings include "encicle".
    </p>

    <h3>Word choices: a worked example</h3>

    <p>
      The very first line, 1:1,  "Alas! Alone she lies: the city" demonstrates substitution.
      The original verb is more literally "sits",
      and most prose-oriented English translations use this.
      But in English, we tend to think of such a city as "lying desolate" rather than "sitting desolate".  For us, "lie" is a better poetic metaphor than "sits".
      Further, and more subtly, the same verb is used a little later in the middle of v.3.  This would be "&hellip;sits among the nations".
      But again, that doesn't really work in English.  Yet neither would "&hellip;lies among the nations".
    </p>

    <p>
      Part of the problem is that the original "sits" has an additional shade of meaning that can incorporate something like "dwells" or "resides".
      And the two uses here allow one common Hebrew verb to express both ideas.
      But no single English verb captures this.
    </p>

    <p>
      So in v.3, I use "lodges among the nations".
      While I am now using two different verbs where the original has one common verb,
      nevertheless the pairing of "lies" and "lodges" lends alliterative commonality and association.
    </p>

    <p>
      The same verb recurs at 4:21 but now in a sarcastic context,
      applied to the enemy Daughter Edom.<?php Footnote('Dobbs-Allsopp (2012), p.137.'); ?>
    </p>

    <p>
      A related point is that the same verb occurs at 3:28,
      and it would be advantageous to preserve this commonality if reasonably possible.
    </p>

    <h3>YHWH, Adonai, <span class='yhwh'>Lord</span> and Lord</h3>

    <p>
      Most English translations face the issue of representing
      two closely related but different Hebrew words that name God:
      "YHWH" and "Adonai".
      This has considerations both for writing/reading and for recitation.
      Alter's 2019 translation writes, respectively, "<span class='yhwh'>Lord</span>" and "Master",
      but this seems unsatisfactory because "Master" is outside normal Christian practice.
      Yet using "Yahweh" or, worse, "Jehovah" seems unsatisfactory in a version
      designed for recitation,
      as Jewish custom avoids saying the Divine Name "YHWH",
      instead saying the other word "Adonai".<?php
        Footnote('Sometimes "HaShem", "the Name", is pronounced.');
      ?>
      But this Jewish custom at least results a common-sounding word for recitation.
      Some English translations bring into play terms such as "the Lord Almighty".
      Again, this long-windedness is unsatisfactory for a version
      which must translate in as compact and concise a manner as possible.
    </p>

    <p>
      The net result here
      is firstly to adopt the usual present-day practice of writing
      "YHWH" as "<span class='yhwh'>Lord</span>" (small capitals) and "Adonai" as "Lord" (normal case).
      And because "Adonai" generally appears in a grammatically possessive context
      (the underlying noun being "Adon"),
      it is secondarily here represented as "<em>my</em> Lord" or "<em>our</em> Lord",
      so that the subtle written difference is also audibly present in recitation.<?php
        Footnote('"Much Ado about Nothing"?
          Reducing further the concern about such detail
          is that one of the earliest Hebrew witnesses,
          Dead Sea Scrolls 4QLam,
          itself shows some variation of YHWH/Adonai usage
          from the authoritative but later Masoretic Text.')
      ?>
    </p>

    <h3>Other examples</h3>

    <ul>
    <li>
      Chapters 2 and 4 share the verb "pour"/"disgorge" as a thread,
      internal to each and common acoss them,
      and absent from other chapters.
      But whereas "pour" suits chapter&nbsp;2 and "disgorge" suits chapter&nbsp;4,
      neither seems quite right for the other chapter,
      nor did any other verb seem right for both.
    </li>

    <li>
      At 4:16 very few English translations capture this verse's use of "face" in the Hebrew.
      Even fewer capture its double use.
      <a href="https://www.biblegateway.com/passage/?search=lamentations+4&version=YLT" target="_blank">Young's Literal Translation</a>
      and the <a href="https://biblehub.com/lsv/lamentations/4.htm" target="_blank">Literal Standard Version</a>
      are two notable exceptions.
      The Hebrew even reinforces this aspect by opening both couplets with "face":
      "[The] face&hellip; [the] faces&hellip;".
      This seems an astonishing omission,
      as the concept of "the face (or countenance) of the <span class="yhwh">Lord</span>"
      seems theologically important in the Torah.
      This version makes sure to include it.<?php
        Footnote('Alas, the acrostic constraint prevents its use to head the first line, so we instead position that repetition at the line ends.');
      ?>
    </li>

    <li>
      At 5:18, desolate Zion is vividly pictured as occupied by wild animals.
      Other translations variously use "foxes" or "jackals";
      ancient zoological classifications are sometimes ambiguous to us.
      Here, I use "haunt of hyenas", which maintains the broad zoological classification
      and allows the use of a suitably unsettling alliterative expression.
    </li>
    </ul>

