    <h2>Word choices</h2>

    <p>
      Lamentations was written in contemporary language.
      Accordingly this version sometimes uses words and terms that are relatively modern
      or that have a modern edge.
      Examples include: "zero in" (1:22), "blitzed" (2:2), "hell-bent" (2:8), "slow-clap" (2:15), "snide-song" (3:63), "zilch" (3:64), "ziplock" (3:65), "firestorm" (4:11), "boozed" (4:21), "up to our necks" (5:5).
    </p>

    <p>
      Meanwhile the verb "zoned" (2:22) is old, whose meanings include "encicle".
      And "ranked" (4:2) also now carries an appropriate modern slant of disdain.
    </p>

    <h3>YHWH, Adonai, <span class='yhwh'>Lord</span> and Lord</h3>

    <p>
      Most English translations face the issue of representing
      two closely related but different Hebrew words that name God:
      "YHWH" and "Adonai".
      This has considerations both for writing/reading and for recitation.
      Alter's 2019 translation writes, respectively, "<span class='yhwh'>Lord</span>" and "Master".
    </p>

    <p>
      Jewish custom avoids saying the Divine Name "YHWH",
      instead saying the other word "Adonai".<?php
        Footnote('Sometimes "HaShem", "the Name", is pronounced.');
      ?>
      This results a common-sounding word for recitation.
      Representing "YHWH" as "Yahweh" or, worse, "Jehovah"
      seems unsatisfactory in a version designed for recitation,
      And representing "Adonai" as "Master" seems unsatisfactory
      because it is outside normal Christian practice.
      Some English translations bring "Sovereign" into play,
      especially when the Hebrew is "Adonai YHWH",
      which becomes "Sovereign <span class='yhwh'>Lord</span>".
      But in Lamentations that double term doesn't appear,
      so we needn't consider that slightly awkward wording.
    </p>

    <p>
      The net result here
      is firstly to adopt the usual present-day practice of writing
      "YHWH" as "<span class='yhwh'>Lord</span>" (small capitals)
      and "Adonai" as "Lord" (normal case).
      And then because "Adonai" generally appears in a grammatically possessive context
      (the underlying noun being "Adon"),
      it is secondarily here represented as "<em>my</em> Lord" or "<em>our</em> Lord",
      so that the subtle written difference is also audibly present in recitation.<?php
        Footnote('"Much Ado about Nothing"?
          Reducing further the concern about such detail
          is that one of the earliest Hebrew witnesses,
          Dead Sea Scrolls 4QLam,
          itself shows some variation of YHWH/Adonai usage
          from the authoritative but later Masoretic Text. Kotz&eacute; (2011), p.115.')
      ?>
    </p>

    <h3>Daughter Zion</h3>

    <p>
      Central to chapters&nbsp;1 and 2 is "Daughter Zion" ("Daughter Jerusalem").
      The "Daughter&nbsp;X" metaphor extends across all the first four chapters
      with "Daughter Judah" and "Daughter People".
      In chapter&nbsp;4 it is given a further, but sarcastic, twist
      of <i><span lang="de">schadenfreude</span></i>
      with "Daughter Edom".
    </p>

    <p>
      Yet this seemingly simple form of wording, in Hebrew <em>bat Tzion</em>,
      is notoriously difficult to translate across
      from Ancient Near East cultures into modern Western cultures.
      Many older translations had opted for "Daughter&nbsp;<em>of</em> X".
    </p>

    <p>
      Perhaps the majority of recent translations and commentators specifically avoid the inserted English "of".
      There seems a widely agreed recognition that this grammatical construction
      is a conceptual metaphor,
      yielding multiple meanings for God's relationship to the people, the land, and the Temple at its sacred centre,
      and is not to be understood in the sense of daughter <em>of</em> Zion, but rather in the sense of Zion <em>as</em> daughter.<?php Footnote('Adelman (2021).'); ?>
      This is not merely a secular-usage "Land of X" or "City of X" or "People of X";
      rather, all these concepts are wrapped together, and in relationship to God, as "Daughter X".
      Further, an attempt to retain "of" stretches to breaking point
      of unnecessary obscurity
      in what would have to become "Daughter [of my] People" (2:11, 3:48, 4:3,6,10).
    </p>

    <p>
      With the "Daughter" (<em>bat</em>) component, most translations keep this.
      Nevertheless some commentators use other words for illustration,
      not least to reflect, in their context of study, that the Hebrew <em>bat Tzion</em>
      has a wider semantic range than its English representation "Daughter Zion".
      Further, the sense of endearment in "daughter" also varies among commentators. Adelman (2021) and Berlin (2004), p.12, endorse it;  Goldingay (2022), p.60, downplays it.
    </p>

    <p>
      A few examples.
      The NIV has "Daughter X" (upper-case 'D');
      the NABBRE and NIV (also Provan (2016), p.41) have "daughter X" (lower-case 'd');
      Berlin (2004) has "Dear X" (which heightens the sarcasm in chapter&nbsp;4's "Dear Edom");
      Hens-Piazza (2017) has "Woman Zion";
      Goldingay (2022) has "Ms.&nbsp;Zion" (Miss/Mistress).
      Those recent versions which omit the "of" generally capitalise "Daughter" (or their equivalent word),
      making it a proper noun, with a sense not merely of description but also of title.
    </p>

    <p>
      For a deeper introduction to this topic, both grammatical and cross-cultural,
      see the three page discussion in Berlin (2004), pp.10&ndash;12
      and the two page discussion in Berman (2023), p.34<i>b</i>&ndash;36<i>a</i>.
    </p>

    <p>
      This version,
      with its slants towards recovery of the original poetry and to present-day dramatic usage (including worship contexts),
      specifically follows the "Daughter X" route.
      In a couple of instances the pervasive challenge of the acrostic constraint
      have necessitated an inversion
      from "Daughter Jerusalem" to "Jerusalem-Daughter" (2:10)
      and from "Daughter Zion" to "Zion-Daughter" (4:22).<?php
        Footnote('As a minor beneficial side-effect
          this can occasionally aid recitation compactness
          where strong <i>qinah</i> beats are a scarce commodity.
          While "<strong>daugh</strong>ter Je<strong>ru</strong>salem" still normally uses two beats,
          it can be eased into a single beat (e.g. "daughter Je<strong>ru</strong>salem")
          if necessary, such as at 2:8.');
      ?>
    </p>

    <h3>Sit/lodge/lie: a worked example</h3>

    <p>
      The very first line "[Alas!] Alone she sits:&hellip;" ignites a translational ambiguity in vocabulary.
      The original verb may be legitimately represented as 'sit', 'abide', 'lodge', 'reside' or 'lie'.
      But no single English verb naturally embraces this range.
      From the very start of this version, I had opted for "Alone she lies"
      (a) reasoning (with hindsight, weakly) that in English
      we tend to think of a city as "lying" rather than "sitting" in a landscape
      and (b) liking the alone/lies alliteration.
      But almost immediately at 1:3 a problem arose
      in describing the exiled, deported populace
      which would be "she lies [among the nations]".
      So I used a different but nonetheless alliterative verb "she lodges [among the nations]".
      I left it at that, having become perhaps over-attached to 1:1 'lies',
      and despite knowing full well the importance of poetic resonance
      across the text as a word recurs in different contexts.
    </p>

    <p>
      A long time later, I began to notice that same Hebrew verb
      (although often obscured in existing English translations)
      recurring elsewhere, particularly:
    </p>

    <ul>
      <li>
        3:28: at almost the centre of the book, applied to the man/<em>geber</em><?php Footnote ('Goldingay (2022), p.143.'); ?>
        which suggests a posture of 'sitting' rather than 'lying'
      </li>
      <li>
        4:21: applied in a sarcastic context to the enemy Daughter Edom<?php Footnote('Dobbs-Allsopp (2012), p.137.'); ?>
      </li>
      <li>
        5:19: at almost the very end,
        and in a repetition and implied resonance
        astonishingly overlooked by almost all translations,
        applied to God himself, where a late revision specifically to handle this
        was about to become
        "And you, <span class="yhwh">Lord</span>, forever lie [enthroned]".
      </li>
    </ul>

    <p>
      Surely this threaded repetition of the verb
      was intentional by the original author(s) and editor(s).
      While translation inevitably forces some compromises in some places,
      this instance seems too important to fudge,
      particularly in our avowedly poetically-driven framework.
    </p>

    <p>
      But in this 5:19 case, I immediately realised that
      in saying of God that he "forever lies"
      creates a new problem of ambiguity,
      this time within English: "God forever [tells] lies".
      However unintentional, this would clearly be unsatisfactory!
    </p>

    <p>
      As a result, I reworked the verb across the text to become 'sit'.
    </p>

    <h3>Other examples</h3>

    <ul>
    <li>
      Chapters 2 and 4 share the verb "pour"/"disgorge" as a thread,
      internal to each and common across them,
      and absent from other chapters.
      But whereas "pour" suits chapter&nbsp;2 and "disgorge" suits chapter&nbsp;4,
      neither seems quite right for the other chapter,
      nor did any other verb seem right for both.
    </li>

    <li>
      At 4:16 very few English translations capture this verse's use of "face" in the Hebrew.
      Even fewer capture its double use.
      <a href="https://www.biblegateway.com/passage/?search=lamentations+4&version=YLT" target="_blank">Young's Literal Translation</a>
      and the <a href="https://biblehub.com/lsv/lamentations/4.htm" target="_blank">Literal Standard Version</a>
      are two notable exceptions.
      The Hebrew even reinforces this aspect by opening both couplets with "face":
      "[The] face&hellip; [the] faces&hellip;".
      Its absence from most English versions seems an astonishing omission,
      as the concept of "the face (or countenance) of the <span class="yhwh">Lord</span>"
      seems theologically important in the Torah.
      This version makes sure to include it.<?php
        Footnote('Alas, the acrostic constraint prevents its use to head the first line, so we instead position that repetition at the line ends.');
      ?>
    </li>

    <li>
      At 5:18, desolate Zion is vividly pictured as occupied by wild animals.
      Other translations variously use "foxes" or "jackals";
      ancient zoological classifications are sometimes ambiguous to us.
      Here, I use "haunt of hyenas", which maintains the broad zoological classification
      and allows the use of a suitably unsettling alliterative expression.
    </li>
    </ul>

    <h2>Inclusive language</h2>

    <p>
      Contemporary writing and translations rightly veer towards inclusive language, such as preferring "humankind" for "mankind".
      In general, this version adopts that principle.
    </p>

    <p>
      Nevertheless, gender-specificity is a strong feature of some of the Lamentations poetry.
      The "Daughter Zion" and "Daughter Jerusalem" personalisation is integral to chapters 1 and 2.
      Similarly, the "strong man" characterisation recurs through chapter 3.
      In these contexts, attempting to downplay these characteristics
      would seem not only pointless but even counter-productive.
      Accordingly this version maintains this distinction.
    </p>

    <p>
      Particular points include:
    </p>

    <ul>
    <li>
      Chapter&nbsp;3 is primarily about the "strong man", Hebrew <i>geber</i>.
      But it also uses the Hebrew word <i>adam</i> which is
      rather like the English word "man",
      being sometimes generically human and sometimes male-specific.<?php
        Footnote('See, for example, the near-adjacent uses in Genesis.
          Gen.1:27 is the generic
          "God created <i>adam</i>:&hellip; male and female he created them".
          Gen 2:7 is the male-specific
          "The <span class="yhwh">Lord</span> God formed <i>adam</i> from the dust of the ground",
          where the context prepares the way for the further creation of his female partner, Eve.');
      ?>
      Indeed, 3:39 uses both words.
      We translate <i>geber</i> as male-specific "man" (or similar)
      and <i>adam</i> as a generic "one" (or similar).
    </li>

    <li>
      At 4:1&ndash;2 "sons" is used to allow a carrying forward from the Hebrew
      of its "stones&hellip;sons" wordplay
      which is embedded in a larger-scale metaphor in those verses.
      See the footnote there.
    </li>
    </ul>

