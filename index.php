    <p>
    <i>Lamentations is poetry. Not prose.</i>
    </p>

    <p>
    Bible translation, quite rightly, lays great emphasis on capturing meaning of a text
    and of the words that comprise it.
    Within this, there is a range of activity, often characterised as
    <a href="https://en.wikipedia.org/wiki/Dynamic_and_formal_equivalence" target="_blank">dynamic and formal equivalence</a>.

    </p>
    <p>
    Near one end of the spectrum, some translations focus on the meanings of individual words.
    To a first (and very rough) approximation, this can be akin to "cookbook translation by dictionary lookup",
    sprinkled with just sufficient syntactic sugar and lexical seasoning to make it readable in
    the target language.
    This is sometimes called "formal equivalence".
    The classic studying translations tend this way: KJV, RSV, etc.
    </p>
    <p>
    Towards the other end of the spectrum can be a focus on what a more large-scale phrase or full sentence
    meant in its original time and context, then re-presenting that overall phrase or sentence
    in the target language and culture.
    This is sometimes called "dynamic equivalence".
    Examples are "The Living Bible" and "The Message".
    </p>

