    ?>
    Poetic imagery points to experiences that eclipse words.<?php Footnote('Hens-Piazza (2017), p.xlvi.'); ?>
    It can also purposefully exploit ambiguity:
    appearing to say one thing,
    yet simultaneously suggesting something else, even the opposite.
    A case in point here is 1:7&ndash;10:
    do we perceive and receive Daughter Zion as sinner or victim (or both)?
    </p>

    <p>
    Poetry through the ages deploys a range of techniques.
    There is rhyme.  Alliteration.  Rhythm and metre.
    Even the physical feel of aloud-spoken words in the mouth plays a part.
    </p>

    <p>
    Central to the poetry of biblical Hebrew is an extraordinary compactness.
    Words are punchy; phrases short; syntax free from clutter.
    Psalm&nbsp;23's famous five-word
    "The <span class="yhwh">Lord</span> is my shepherd"
    comes from a simply two-word original: "Yahweh ro'i".<?php Footnote("Alter (2019), pp.94&ndash;95."); ?>
    And Lamentations exploits this compactness to dramatic effect.
    </p>

    <p>
    Lamentations also has the acrostic, discarded in most translations.
    But perhaps even more important,
    and also jettisoned in most translations,
    is a forceful, angst-driven rhythm, known as <i>qinah</i>.<?php
      Footnote('The term <i>qinah</i> has two different poetic uses: (a) large-scale: the genre of an entire funeral-eulogy poem; (b) small-scale: a particular 3&ndash;2 rhythm within a poetic line, primarily but not exclusively used within such eulogies.  Middlemas (2021) discusses both: p.2 (large-scale genre), pp.22&ndash;23 (rhythm) and p.33 (their interaction). In our context, we use the rhythmic meaning.');
