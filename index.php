    <p>
    Several psalms use the technique of acrostics in the Hebrew alphabet of 22 letters.
    Many of us may already be familiar with this, lurking, but usually ignored by us, in the background.
    </p>

    <p>
    In the Lamentations anthology of five poems, four are Hebrew-acrostic
    and the third of these is even triple acrostic:
    </p>

    <div class="panel panel--width-17em">
      <div class="poem poem__linewrap poem--acrostic">
        <p>Agonies: I am&nbsp;&hellip;</p>
        <p>Away he has driven&nbsp;&hellip;</p>
        <p>Against me he turns&nbsp;&hellip;</p>
        <p>&nbsp;</p>
        <p>Breaking my bones&nbsp;&hellip;</p>
        <p>Besieged by him&nbsp;&hellip;</p>
        <p>Bound by him&nbsp;&hellip;</p>
      </div><!-- "poem" -->

      <div class="panel__cite">
          &mdash;Lam.&nbsp;3:1&ndash;6
      </div><!-- "panel__cite" -->
    </div><!-- "panel" -->

    <p>
    The final poem, while not Hebrew-acrostic, nevertheless also has 22 stanzas
    and some see a surprising hidden acrostic in its final four stanzas,
    whose principle is also made manifest in this rendering.
    </p>

    <p>
    An aim of this new version is to capture that foundational acrostic technique
    that almost all modern translations lack,
    <a href="http://catholicbible.online/knox/OT/Lam" target="_blank">that of Ronald Knox</a>
    being an exception.<?php
      Footnote('Unfortunately the Knox translation de-regularises the all-important opening \'A\' word of chapters 1, 2 and 4. These ought to be consistently "Alas" or equivalent but instead become the inconsistent set "Alone", "Alas" and "All".');
    ?>
    </p>

    <p>
    In his magesterial translation of the Hebrew Bible,
    <a href="https://en.wikipedia.org/wiki/Robert_Alter" target="_blank">Robert Alter</a> notes:
<i>"This [acrostic] form leads to even more syntactic inversions than is common in biblical Hebrew,
    with the object of the verb 'fronted' at the beginning of many lines,
    but the poet exploits this pattern for expressive emphasis."</i><?php
      Footnote('Alter, Robert (2019) "The Hebrew Bible: Volume 3: The Writings", footnote to Lam.&nbsp;3:1. p.657. 978-0-393-29249-7');
    ?>
    Again, a prose-biased quest for "meaning" might miss this.
    But it can come across with authentic power in the poem,
    including in English translation or paraphrase.
    </p>

    <p>
    In mapping Hebrew's 22 letters onto the western alphabet's 26,
    four letters need omitting.<?php
      Footnote('The further subtlety of two different versions of the Hebrew alphabet having
        a reversal of two of its letters, with some chapters using one and some the other,
        is deemed a geek-fest too far for this particular exercise.');
    ?>
    Knox chose to omit the final four: W&ndash;Z.
    But an appeal of alphabetic-acrostic poetry is its all-encompassing
    "A&nbsp;to&nbsp;Z" sense of totality:
    in the case of the people of Lamentations, the totality of the destruction of their beloved Jerusalem;
    the <span class="acrostic-letter">A</span>nnihilation of <span class="acrostic-letter">Z</span>ion.
    Indeed, the text of the second poem is itself book-ended, "A&nbsp;to&nbsp;Z" style,
    by "the day of the <span class="yhwh">Lord</span>'s anger".
    </p>
    <p>
    So isn't this "aleph to tav", "A&nbsp;to&nbsp;Z" totality equally valid as an integral,
    even essential, component of "meaning"?
    Whatever necessary four letters are chosen for omission,
    "A" and "Z" surely remain indispensable.
    </p>

