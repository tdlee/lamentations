    <h2>Can scripture be "performance poetry"?</h2>

    <p>
    Yes!  See the Psalms.  But even more so, Lamentations.
    Lamentations leaps into life when released from
    the bondage of our preconceptions.
    </p>

    <!-- panel -->
    <div class="sidebar__right--width-28em">
      <div class="panel">
        <div class="verse">
  Write loyal cantons of contemn&egrave;d love
    and sing them loud even in the dead of night;
  Halloo your name to the reverberate hills
    and make the babbling gossip of the air
    cry out "O, list'n to us!" O, you should not rest
    between the elements of air and earth
    but you should pity me.
        </div><!-- "verse" -->
        <div class="panel__cite">
            &mdash;William Shakespeare "Twelfth Night" <i>(adapted)</i>
        </div><!-- "panel__cite" -->

      </div><!-- "panel" -->
    </div><!-- "sidebar" -->
    <!-- panel -->
    <p>
    Might our reading of scripture risk actually stifling it?
    We sit in comfortable chairs. We are silent.
    We smother it under a prose-shaped bias that pre-disposes our expectations:
    the earnest, spiritual quest for "the meaning".
    And we then, subconsciously, expect scripture to conform and behave
    in accordance with our pre-formed expectation of our "meaning".
    </p>
    <p>
    But <em>this</em> version aims to <em>demand</em> us
    leap up and perform it aloud.
    </p>

    <p>
    Poetry is far, far more than its mere component words and phrases.
    Prose often (not always, of course) attempts to tie down meaning to a particular intent.
    </p>

    <p>
    By contrast to this "pinning down" of prose,
    the very form and nature of poetry can take us in the opposite direction:
    the act of performing it opens out the edges of meaning; it invites and encourages the reader to explore an open landscape.
    </p>

    <p>
    Poetry through the ages deploys a range of techniques.
    There is rhyme.  Metre.  Alliteration.
    Even the physical feel of aloud-spoken words in the mouth plays a part.
    </p>

    <p>
    Lamentations also has the acrostic, discarded in most translations.
    But perhaps even more important,
    and also jettisoned in most translations,
    is a forceful, angst-driven rhythm, known as 'qinah'.
    </p>

    <h3>Qinah&hellip;</h3>

    <p>
    <i>Lamentations is poetry. Not prose.</i>
    </p>

    <p>
    Declaim it aloud!
    Vital to the Hebrew verse of Lamentations is its three&ndash;two <i>qinah</i> stress pattern pervading all but the final chapter.
    Hebrew poetry can be tightly concise compared to English equivalents
    which tend to wax more loquaciously expansive.
    This version aims for that original tight conciseness.
    </p>

    <p>
      Hit those stresses.  This is rap.  This is beat.
      Or, in more refined society,
      "<a href="https://en.wikipedia.org/wiki/Sprung_rhythm" target="_blank">sprung rhythm</a> with attitude".
    </p>

    <div class="panel panel--width-26em">
      <div class="poem">
        <p>
          &emsp;&emsp;&emsp;&emsp;<strong>Ag</strong>onies:&emsp;&emsp;<strong>I</strong> am the man <strong>seared</strong><br>
          by the <strong>rod</strong> &emsp;of his <strong>wrath</strong>;
        </p>
        <p>
          &emsp;&emsp;&emsp;A<strong>way</strong> he has <strong>dri</strong>ven, force-<strong>marched</strong> me<br>
          &emsp;&emsp;in <strong>dark</strong>ness, no <strong>light</strong>;
        </p>
        <p>
          &emsp;&emsp;&emsp;A<strong>gainst</strong> me he <strong>turns</strong> &emsp;his&emsp; <strong>hand</strong><br>
          &emsp;from <strong>day</strong>-dawn to <strong>dusk</strong>;
        </p>
      </div><!-- "poem" -->
    </div><!-- "panel" -->

    <p>
    And we might observe that the overall "3+2" count of the line matches the pentameter, so familiar in English poetry.
    </p>

    <p>
    It is sadly rare for an English translation
    to capture this condensed rhythmic vitality
    of the scriptural text.
    Admirers of the translation by
    <a href="https://en.wikipedia.org/wiki/Robert_Alter" target="_blank">Robert Alter</a> will know
    his work to capture it;
    see, for example, his Psalm&nbsp;29 and Genesis&nbsp;1.
    Also notable is the
    <a href="http://www.usccb.org/bible/" target="_blank">New American Bible Revised Edition (NABRE)</a>;
    see their <a href="http://www.usccb.org/bible/lamentations/0" target="_blank">Introduction to Lamentations</a>
    and their <a href="http://www.usccb.org/bible/lamentations/1" target="_blank">online translation</a>.
    By coming to such texts from the poetic angle, some of this can be regained.
    </p>
    <p>
    I thoroughly recommend these translations.
    But note that both had other constraints which ruled against using acrostics.
    </p>

