    <h2>Some random details</h2>

    <ul>
      <li>
        Although the opening "Alas!", Hebrew <i>ekah</i>, of chapters one, two and four is a word,
        a leading commentator describes this as <em>opening primordial scream of "Eikhaaah!"</em>.<?php
          Footnote('In full:
            <em>
            The movement from the sequence\'s opening primordial scream of "Eikhaaah!"
            (which is as much a token of glossolalia or pure sound&mdash;the kind of preferential ejaculation to which language is so often reduced by extreme suffering&mdash;as a semantically weighted word [NRSV\'s "How"])
            to the articulate speech of the rest of the poetry&hellip;similarly well symbolizes these poems\' reclamation of language from the wordless garble of anguished speech.
            </em>
            Dobbs-Allsopp (2012), p.33.');
          ?>
          A rather more dramatic opening than simply reciting "Alas".
      </li>

      <li>
        Drama, of course, thrives on contrast and tension.
        At several points Lamentations pulls against other biblical passages,
        sometimes in seemingly deliberate opposition.
        The introduction to chapter&nbsp;4 gives two examples;
        perhaps more subtle is that at the opening of chapter&nbsp;3.
        Can this be worked in?
        ("Your covenant promise with us was good <em>X</em>, but we are now in opposite state&nbsp;<em>Y</em>".)
      </li>

      <li>
        Within its 22 verses, the first chapter may contain a dozen or so allusions to the Egyptian captivity.<?php Footnote('Dobbs-Allsopp (2012), pp.75&ndash;77'); ?>
        That event was of crucial importance to the original audience, so would be recognised, and in a profound manner.
        But to a present-day western audience,
        while the event (Moses, "Red Sea", Exodus wanderings, etc.) might be known about, but as a relatively remote story,
        these deep allusions are probably entirely missed.
        So staging might attempt to convey something of that.
      </li>

      <li>
        The tape-measure is a tool for creation and construction.
        But in the opening section of chapter 2
        God uses it (2:8) in setting about the destruction of his very own dwelling-place.
        "Demolition itself requires careful planning."<?php Footnote('Provan (2016), p.68.'); ?>
      </li>

      <li>
        3:18: Note the double meaning, mentioned in the footnote.
        The text has had to lean one way;
        stage gesture can indicate the more accusatory edge.
      </li>

      <li>
        <em>(t.b.c.)</em>
      </li>
    </ul>

    <h2>A forerunner</h2>

    <p>
      Back in 1999, long before I approached any work whatsoever on Lamentations,
      <a href="https://cairn.edu/faculty/brian-toews/" target="_blank">Brian Toews</a> of Cairn University (at the time called Philadelphia College of Bible)
      had already staged it with and for his students in their coursework.
      He used the traditional Authorised (King James) text.
      So, of course, no acrostics; no <i>qinah</i>.
      For the theatrically minded,
      his script with stage directions
      and his subsequent report to a conference through Asbury Seminary are worth reading:
