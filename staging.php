    <h1>Lamentations: Musings on staging</h1>

    <?php
      include('chapter-links.php');
    ?>

    <p>
      Lamentations springs to life when its poetic form is appreciated and respected.
      Poetry is performance.
      And this can include, perhaps especially so, staged readings or even theatrical production.
    </p>

    <p>
      "Look!" and "notice!" comprise a thread running through each chapter of Lamentations and across all of them:
      see the <a href="translation-notes.php">translation notes</a>.
      And it is all about bringing vivid pictures into the mind.
      Visual staging seems to be not only an option, but encouraged.
    </p>

    <p>
      In preparing this version of the text over the course of a few years
      various thoughts and ideas have come to mind,
      ("I wonder how a producer/director would do that?")
      bidden and unbidden, considered and random, about such aspects.
      They are given here simply to spark further imagination by the potential producer/director.
    </p>

    <p>
      Perhaps the foundational consideration is that Lamentations is both aural <em>and</em> visual, hence the impetus towards theatrical staging.
      That "visual" is not only "the mind's eye"
      extrapolating from the text and staging,
      but real, not least in seeing the alphabetic acrostic at work.
    </p>

    <h2>Visual</h2>

    <p>
      The alphabetic acrostic of the first four chapters,
      and its absence in the last,
      is an essential foundation.
      This works visually: seeing the passing alphabet is vital.
      Perhaps an obvious method is projecting the letters onto a screen.
      I was re-assured to discover that this is also what a forerunner did: see below.
      In his case (which was a univeristy biblical studies course)
      he was able to use the Hebrew alphabet.
      In our case, where our English alphabet is used, that wouldn't work;
      simply use our alphabet.<?php
        Footnote('But naturally, if the environment has a strong Hebrew or Jewish aspect,
          then the Hebrew alphabet could additionally be displayed,
          on the understanding that this is not formal transliteration.');
      ?>
      Of course, we have the added twist of having to omit four letters.
      This probably requires a programme note,
      perhaps supplemented by a reminder by something like
      using our full alphabet but greying out the omitted letters.
    </p>

    <p>
      Some aspects of the text lend themselves to visual demonstration.
      For example, chapter&nbsp;2 initially looks like an audio monologue
      (with Daughter Zion speaking only at the end).
      But v.13 sets up a chain of "who" questions:
      "to what can I liken you?&nbsp;/&nbsp;how advocate?&nbsp;/&nbsp;who could heal you?"
      which leads to a sweeping panorama of three iniital candidates on display:
      prophets (14), passers-by (15) and even human enemies (16).
      Finally there is a fourth candidate, God himself.
      But he, already identified as "enemy" (5), "has done what he planned,&nbsp;/&nbsp;fulfilled his threat&nbsp;(17)."<?php
        Footnote('See Dobbs-Allsopp (2012), pp.97&ndash;98; O\'Connor (2002), pp.40&ndash;41.');
      ?>
    </p>

    <p>
      Similarly, if the staging of chapter&nbsp;3 involves some sort of scenery and props
      then its 'L'-acrostic lines, 28&ndash;30, lend themselves to re-visiting complaint scenes from earlier verses:<?php Footnote('Hens-Piazza (2017), pp.49&ndash;50.'); ?>
    </p>

    <ul>
      <li>
        28: "Lonesome let him sit and in silence": <i>cf.</i> 2&ndash;8: the dark isolation and confinement.
      </li>
      <li>
        29: "Lend he his mouth to the dust": <i>cf.</i> 16: "Fracturing my teeth on gravel, he ground me in dust".
      </li>
      <li>
        30: "Lend he his cheek to the assailant"; take his fill of disgrace: <i>cf.</i> 11&ndash;14: the physical attacks and laughing-stock.
      </li>
    </ul>

    <p>
      Inclusion of a wall at or towards one side of the stage could be beneficial.
      Walling is explicitly mentioned in 3:7 and 3:9
      where it acts as a barrier to prayer.<?php
        Footnote('The acrostic constraint in this English version has required rendering this with the verb "confine". Nevertheless "wall" is explicitly retained in the overall rendering of the 3:7&ndash;9 verse-set.');
      ?>
      At 3:44 God metaphorically walls himself off from prayer by a cloud.
      At the opening of chapter&nbsp;5 ("Recall, look, notice")
      the wall can act as a link, now communal.<?php Footnote('Hens-Piazza (2017), p.77.'); ?>
      To a present-day audience,
      visual resonance with the well-known Western Wall of the Temple Mount complex
      then springs to mind
      (although more literally that might be considered anachronistic
      as it dates from after the time of Lamentations).
    </p>

    <p>
      The transition from chapter&nbsp;2 into and through chapter&nbsp;3
      has visual potential.
      Chapters&nbsp;1 and 2 have been portraying Daughter Zion in great distress.
      The audience is longing for her rescue.
      Now chapter&nbsp;3 opens with the arrival of the "strong man",
      announcing "I am the man":
      the classic set-up for the arrival of the "rescuing hero",
      the mighty soldier building expectations of protection.<?php Footnote('Hens-Piazza (2017), p.40.'); ?>
      Leaving Daughter Zion on stage can heighten this.
      But the reality will be far different.
      Instead of rescue he is utterly self-absorbed;
      he doesn't even acknowledge her (as "Daughter People", 48, and "daughters of my city", 51)
      until well past the two-thirds point of the scene.
      And then?
      He immediately reverts to self-absorption,
      "<em>my</em> enemies ensared <em>me</em>",
      and continues to ignore her.
      He has recited that central passage about hope&hellip;yet abandons her, hopeless.
    </p>

    <p>
      The opening of chapter&nbsp;3 contrasts against Psalm&nbsp;23;
      see the chapter introduction.
      Perhaps the psalm text could be projected,
      then step-by-step erased as those verses progress.
    </p>

    <p>
      Also:
    </p>

    <ul>
      <li>
        The entire "look"/"notice" thread.
        And although at 3:1 this version was unable to incorporate the original "looked at (known, experienced) affliction", its threefold "I/me" opening seems to demand visual gesture to command an audience to look and notice.
      </li>
      <li>
        4:1&ndash;8: Marked use of colour, augmented by a counter-use of dullness.
      </li>
      <li>
        <em>(t.b.c.)</em>
      </li>
    </ul>

    <h2>Characterisation and voicing</h2>

    <p>
      In this translation of <cite>Lamentations</cite>
      we resist the temptation to delve too far into
      what interpretation any individual stage production might bring.
      Nevertheless it seems worth mentioning that throughout the Dobbs-Allsopp (2012) commentary
      there are some very useful thoughts about not only characters
      and possible motivations and thoughts, but also groups.
      For example:
    </p>

    <ul>
    <li>
      Chorus (chap.4): While chapter&nbsp;4 is mostly narration,
      nevertheless it is about various groups of people.
      Then at v17 a chorus "we" speaks up.<?php
        Footnote('Dobbs-Allsopp (2012), pp.134&ndash;137: "<em>Excursus</em>: The Choral Lyric".');
      ?>
    </li>

    <li>
      Chorus (chap.5): The embryonic chorus at the end of chapter&nbsp;4
      sets up the full chorus of chapter&nbsp;5.
      Verses 2&ndash;16 constitute a piling up catalogue of brief but pointed complaints
      from a wide cross-section of the oppressed people.
      They are meant to pull us, the passive audience, into the story.
      So rather than being traditionally staged,
      consider scattered actors as representatives
      voicing these verses from <em>within</em> the audience.
    </li>

    <li>
      In chapter&nbsp;2, although Daughter Zion's speech at the end is short, just three verses 20&ndash;22,
      it is stinging, accusing God directly: "the terrors you summoned!" (22<em>a</em>).
      One might then imagine her turning on her heels and walking way,
      now narrator-like, with falling voice:
      "on the day of the <span class="yhwh">Lord</span>'s anger&hellip;my enemy annihilated" (22<em>b,c</em>).<?php Footnote('Dobbs-Allsopp (2012), pp.102&ndash;103.'); ?>
    </li>

    <li>
      <em>(t.b.c.)</em>
    </li>
    </ul>

    <p>
      Chapters&nbsp;1, 2 and 4 strongly feature a narrator.
      The character in chapter&nbsp;1 may be seen as largely objective,
      reporting <em>about</em> what they witness.
      Alternatively they may be viewed as more involved:
      Berman (2023) uses the term "pastoral mentor".
      The character in chapter&nbsp;4 may be seen as much more involved
      <em>with</em> the community and speaking with them.<?php Footnote('Hens-Piazza (2017), p.xlvii') ?>
      So might they be envisaged as two different people?
      Between them, in chapter&nbsp;2, the narrator seems to undergo a change
      within the chapter itself.
      By contrast, then, might this indicate one single overall narrator,
      themselves changed and transformed across the three chapters?<?php
        Footnote('For further discussion see Berman (2023), pp.10&ndash;11.');
      ?>
    </p>

    <h2>Chiastic structures</h2>

    <p>
      On a variety of scales,
      an awareness of
      <a href="https://en.wikipedia.org/wiki/Chiastic_structure" target="_blank">chiastic structure</a>,
      resembling A&nbsp;B&nbsp;C&nbsp;B'&nbsp;A', is useful.
      Its consideration could provide a bearing on set design or other aspects of staging and production.
    </p>

    <h3>Across the book</h3>

    <p>
      There is some resonance, both thematic and lingistic,
      between the outermost chapters 1&nbsp;and&nbsp;5
      and, working inwards, their neighouring chapters 2&nbsp;and&nbsp;4.
    </p>

    <ul>
      <li>
        1: Aftermath of destruction
      </li>
      <li>
        &emsp;2: Destruction
      </li>
      <li>
        &emsp;&emsp;3: Despair to hope and back to despair
      </li>
      <li>
        &emsp;4: Destruction
      </li>
      <li>
        5: Aftermath of destruction
      </li>
    </ul>

    <p>
      From Assis (2009), which is admittedly rather technical,
      cherry-picking these approachable aspects is thoroughly recommended for exploration.
      For example there are some themes that appear in one chapter pair (e.g. 2&nbsp;and&nbsp;4)
      but are absent from the other pair (e.g. 1&nbsp;and&nbsp;5).
    </p>

    <ul>
      <li>
        2 and 4: Famine; God's anger; enemy rejoicing; exposure of sin; responsibility of the prophets; juxtaposition of priests and prophets; king
      </li>
      <li>
        1 and 5: Widowhood; comparison with past ("days of old"); weeping over desolation; feeling of pain and loss; mourning; "no rest"; loss of independence and its leading to servitude; shame and loss of dignity; economic distress
      </li>
    </ul>

    <p>
      Chapters 2 and 4 are also linked by a common verb "pour", absent from other chapters.
      As discussed in the <a href="translation-notes.php">translation notes</a>
      it seemed preferable to represent this as "disgorge" in chapter&nbsp;4.
    </p>

