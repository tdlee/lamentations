    <h1>Lamentations: Musings on staging</h1>

    <?php
      include('chapter-links.php');
    ?>

    <p>
      Lamentations springs to life when its poetic form is appreciated and respected.
      Poetry is performance.
      And this can include, perhaps especially so, staged readings or even theatrical production.
    </p>

    <p>
      "Look!" and "notice!" are a thread running through each chapter of Lamentations and across all of them:
      see the <a href="translation-notes.php">translation notes</a>.
      And it is all about bringing vivid pictures into the mind.
      Visual staging seems to be not only an option, but encouraged.
    </p>

    <p>
      In preparing this version of the text over the course of a few years
      various thoughts and ideas have come to mind,
      ("I wonder how a producer/director would do that?")
      bidden and unbidden, considered and random, about such aspects.
      They are given here simply to spark further imagination by the potential producer/director.
    </p>

    <p>
      Perhaps the foundational consideration is that Lamentations is both aural <em>and</em> visual, hence the impetus towards theatrical staging.
      That "visual" is not only "the mind's eye"
      extrapolating from the text and staging,
      but real, not least in seeing the alphabetic acrostic at work.
    </p>

    <h2>Visual</h2>

    <p>
      The alphabetic acrostic of the first four chapters,
      and its absence in the last,
      is an essential foundation.
      Some way of presenting it is vital;
      perhaps an obvious one is projecting the letters onto a screen.
      I was re-assured to discover that this is also what a forerunner did: see below.
      In his case (which was a univeristy biblical studies course)
      he was able to use the Hebrew alphabet.
      In our case, where our English alphabet is used, that wouldn't work;
      simply use our alphabet.<?php
        Footnote('But naturally, if the environment has a strong Hebrew or Jewish aspect,
          then the Hebrew alphabet could additionally be displayed,
          on the understanding that this is not formal transliteration.');
      ?>
      Of course, we have the added twist of having to omit four letters.
      This probably requires a programme note,
      perhaps supplemented by a reminder by something like
      using our full alphabet but greying out the omitted letters.
    </p>

    <p>
      Some aspects of the text lend themselves to visual demonstration.
      For example, chapter&nbsp;2 initially looks like an audio monologue
      (with Daughter Zion speaking only at the end).
      But v.13 sets up a chain of "who" questions:
      "to what can I liken you?&nbsp;/&nbsp;how advocate?&nbsp;/&nbsp;who could heal you?"
      which leads to a sweeping panorama of three iniital candidates on display:
      prophets (14), passers-by (15) and even human enemies (16).
      Finally there is a fourth candidate, God himself.
      But he, already identified as "enemy" (5), "has done what he planned,&nbsp;/&nbsp;fulfilled his threat."<?php Footnote('See Dobbs-Allsopp (2012), pp.97&ndash;98.'); ?>
    </p>

    <p>
      Also:
    </p>

    <ul>
      <li>
        4:1&ndash;8: Marked use of colour, augmented by a counter-use of dullness.
      </li>
      <li>
        <em>(t.b.c.)</em>
      </li>
    </ul>

    <h2>Recitation</h2>

    <p>
      The 3&ndash;2 <em>qinah</em> pulse is vital to the first four chapters.
      Readers or cast members who have ever performed the iambic pentameter of Shakespeare
      should have little trouble in quickly picking this up.
      Even the differences in how the weak syllabes work
      should present no difficulty:
      simply consider well-known English nursery rhymes
      such as "<cite>Mary, Mary, quite contrary</cite>", "<cite>Humpty Dumpty</cite>", etc.
      where the pulse is maintained
      and the varying number of words simply slot naturally into place.
    </p>

    <p>
      And in the final chapter, where <em>qinah</em> is largely absent,
      although not entirely so,
      the rhythmic pulse can still be used.
    </p>

    <p>
      Be alert to particular words that recur.
      A few examples:
    </p>

    <ul>
      <li>
        "Notice" and "look" form a running thread across the chapters, often paired.
        See the translation notes for further details.
      </li>
      <li>
        Chapter&nbsp;1: recurrent "all", which subtly helps establish the "A&nbsp;to&nbsp;Z" totality of the alphabetic acrostic.
      </li>
      <li>
        Chapter&nbsp;2: "ground" thread; "pour" thread.
      </li>
      <li>
        Biblical poetry often refers to "hand" and "face".
        <ul>
          <li>
            "Hand" occurs several times in each chapter.
          </li>
          <li>
            "Face", while less common, can nevertheless be particularly important,
            not least because of the theological association with "the face of God"
            in the Torah (the foundational first five books of the Bible).
            See 4:16 and its footnotes.
          </li>
        </ul>
      </li>
    </ul>

    <p>
      <em>(t.b.c.)</em>
    </p>

    <h2>Characterisation and voicing</h2>

    <p>
      In chapter&nbsp;2, although Daughter Zion's speech at the end is short, just three verses 20&ndash;22,
      it is stinging, accusing God directly: "the terrors you summoned!" (22<em>a</em>).
      One might then imagine her turning on her heels and walking way,
      now narrator-like, with falling voice:
      "on the day of the <span class="yhwh">Lord</span>'s anger&hellip;my enemy annihilated" (22<em>b,c</em>).<?php Footnote('Dobbs-Allsopp (2012), pp.102&ndash;103.'); ?>
    </p>

    <p>
      <em>(t.b.c.)</em>
    </p>

    <h2>Structure</h2>

    <p>
      Across the text as a whole,
      a less obvious but nevertheless potentially important feature
      is some resonance, both thematic and lingistic,
      between the outermost chapters, 1&nbsp;and&nbsp;5,
      and, working inwards, their neighouring chapters, 2&nbsp;and&nbsp;4.
      This provides the five chapters an overall
      <a href="https://en.wikipedia.org/wiki/Chiastic_structure" target="_blank"><em>chiastic</em></a> framework: A&nbsp;B&nbsp;C&nbsp;B'&nbsp;A':
    </p>

    <ul>
      <li>
        1: Aftermath of destruction
      </li>
      <li>
        &emsp;2: Destruction
      </li>
      <li>
        &emsp;&emsp;3: Despair to hope and back to despair
      </li>
      <li>
        &emsp;4: Destruction
      </li>
      <li>
        5: Aftermath of destruction
      </li>
    </ul>

    <p>
      From Assis (2009), which is admittedly rather technical,
      cherry-picking these approachable aspects is thoroughly recommended for exploration.
    </p>

    <p>
      These are some themes that appear in one chapter pair (e.g. 2&nbsp;and&nbsp;4)
      but are absent from the other pair (e.g. 1&nbsp;and&nbsp;5).
    </p>

    <ul>
      <li>
        2 and 4: Famine; God's anger; enemy rejoicing; exposure of sin; responsibility of the prophets; juxtaposition of priests and prophets; king
      </li>
      <li>
        1 and 5: Widowhood; comparison with past ("days of old"); weeping over desolation; feeling of pain and loss; mourning; "no rest"; loss of independence and its leading to servitude; shame and loss of dignity; economic distress
      </li>
    </ul>

    <p>
      Indeed, an awareness of chiasm throughout is useful on a variety of scales.
      For example, topping and tailing chapter&nbsp;1 as a contrast:
    </p>

    <ul>
      <li>
        1:1: "great with people"/"great among the nations"
      </li>
      <li>
        1:22: "great are my groans"
      </li>
    </ul>

    <p>
      And in chapter&nbsp;2:
    </p>

    <ul>
      <li>
        2:1: "angered/beclouded" verb to open
      </li>
      <li>
        &emsp;2:1: "in the day of his anger"
      </li>
      <li>
        &emsp;&emsp;2:3: "cut off&hellip;every horn of Israel"
      </li>
      <li>
        &emsp;&emsp;2:17: "exalted your foes' horn"
      </li>
      <li>
        &emsp;2:22: "on the day of the <span class="yhwh">Lord</span>'s anger"
      <li>
        2:22: "annihilated" verb to close
      </li>
    </ul>

    <p>
      A further specific example of this mirror-like chiastic structure occurs almost at the extremes of the book.
      If one regards the closing four verses 5:19&ndash;22 as an epilogue,
      so the formal close is 5:18 rather than 5:22,
      then one has the opening
      <em>"Alone she lies: the city once great with people"</em> (1:1)
      returning and reflected in the closing
      <em>"Mount Zion: lying desolate, the haunt of hyenas"</em> (5:18).
    </p>

    <p>
      Consideration of these more hidden features might provide a bearing on set design or other aspects of staging and production.
    </p>

    <h2>Some random details</h2>

    <ul>
      <li>
        Although the opening "Alas!", Hebrew "Ekah!", of chapters one, two and four is a word,
        a leading commentator describes this as <em>opening primordial scream of "Eikhaaah!"</em>.<?php
          Footnote('In full:
            <em>
            The movement from the sequence\'s opening primordial scream of "Eikhaaah!"
            (which is as much a token of glossolalia or pure sound&mdash;the kind of preferential ejaculation to which language is so often reduced by extreme suffering&mdash;as a semantically weighted word [NRSV\'s "How"])
            to the articulate speech of the rest of the poetry&hellip;similarly well symbolizes these poems\' reclamation of language from the wordless garble of anguished speech.
            </em>
            Dobbs-Allsopp (2012), p.33.');
          ?>
          A rather more dramatic opening than simply reciting "Alas".
      </li>

      <li>
        Drama, of course, thrives on contrast and tension.
        At several points Lamentations pulls against other biblical passages,
        sometimes in seemingly deliberate opposition.
        The introduction to chapter&nbsp;4 gives two examples;
        perhaps more subtle is that at the opening of chapter&nbsp;3.
        Can this be worked in?
        ("Your covenant promise with us was good <em>X</em>, but we are now in opposite state&nbsp;<em>Y</em>".)
      </li>

      <li>
        Within its 22 verses, the first chapter may contain no fewer than 12 allusions to the Egyptian captivity.<?php Footnote('Dobbs-Allsopp (2012), pp.75&ndash;77'); ?>
        That event was of crucial importance to the original audience, so would be recognised, and in a profound manner.
        But to a present-day western audience,
        while the event (Moses, "Red Sea", Exodus wanderings, etc.) might be known about, but as a relatively remote story,
        these deep allusions are probably entirely missed.
        So staging might attempt to convey something of that.
      </li>

      <li>
        The tape-measure is a tool for creation and construction.
        But in the opening section of chapter 2
        God uses it (2:8) in setting about the destruction of his very own dwelling-place.
        "Demolition itself requires careful planning."<?php Footnote('Provan (2016), p.68.'); ?>
      </li>

      <li>
        3:18: Note the double meaning, mentioned in the footnote.
        The text has had to lean one way;
        stage gesture can indicate the more accusatory edge.
      </li>

      <li>
        <em>(t.b.c.)</em>
      </li>
    </ul>

    <h2>A forerunner</h2>

    <p>
      Back in 1999, long before I approached any work whatsoever on Lamentations,
      <a href="https://cairn.edu/faculty/brian-toews/" target="_blank">Brian Toews</a> of Cairn University (at the time called Philadelphia College of Bible)
      had already staged it with and for his students in their coursework.
      He used the traditional Authorised (King James) text.
      So, of course, no acrostics; no <em>qinah</em>.
      For the theatrically minded,
      his script with stage directions
      and his subsequent report to a conference through Asbury Seminary are worth reading:
